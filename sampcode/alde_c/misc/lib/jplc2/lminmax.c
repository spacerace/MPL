#include "defs.h"

/************************************************************************/
	long
lmax(x, y)			/* return larger of x and y, long	*/

/*----------------------------------------------------------------------*/
long x, y;
{
	return MAX(x,y);
}

/************************************************************************/
	long
lmin(x, y)			/* return lesser of x and y, long	*/

/*----------------------------------------------------------------------*/
long x, y;
{
	return MIN(x,y);
}
